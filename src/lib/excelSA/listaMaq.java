package lib.excelSA;

public class listaMaq {
	public String responsable;
	public String maquinaria;
	public String implemento;
	public String cambio;
	public String marcha;
	public String presion;
	public String velocidad;
	public String getResponsable() {
		return responsable;
	}
	public void setResponsable(String responsable) {
		this.responsable = responsable;
	}
	public String getMaquinaria() {
		return maquinaria;
	}
	public void setMaquinaria(String maquinaria) {
		this.maquinaria = maquinaria;
	}
	public String getImplemento() {
		return implemento;
	}
	public void setImplemento(String implemento) {
		this.implemento = implemento;
	}
	public String getCambio() {
		return cambio;
	}
	public void setCambio(String cambio) {
		this.cambio = cambio;
	}
	public String getMarcha() {
		return marcha;
	}
	public void setMarcha(String marcha) {
		this.marcha = marcha;
	}
	public String getPresion() {
		return presion;
	}
	public void setPresion(String presion) {
		this.presion = presion;
	}
	public String getVelocidad() {
		return velocidad;
	}
	public void setVelocidad(String velocidad) {
		this.velocidad = velocidad;
	}
}
