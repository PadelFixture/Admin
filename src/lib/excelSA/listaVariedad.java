package lib.excelSA;

public class listaVariedad {
	public String nombreVariedad;
	public String mercado;
	public String fechaEstCosecha;
	public String fechaViable;
	public String diferencia;
	public float maxCerencia;
	public float maxHr;
	public String getNombreVariedad() {
		return nombreVariedad;
	}
	public void setNombreVariedad(String nombreVariedad) {
		this.nombreVariedad = nombreVariedad;
	}
	public String getMercado() {
		return mercado;
	}
	public void setMercado(String mercado) {
		this.mercado = mercado;
	}
	public String getFechaEstCosecha() {
		return fechaEstCosecha;
	}
	public void setFechaEstCosecha(String fechaEstCosecha) {
		this.fechaEstCosecha = fechaEstCosecha;
	}
	public String getFechaViable() {
		return fechaViable;
	}
	public void setFechaViable(String fechaViable) {
		this.fechaViable = fechaViable;
	}
	public String getDiferencia() {
		return diferencia;
	}
	public void setDiferencia(String diferencia) {
		this.diferencia = diferencia;
	}
	public float getMaxCerencia() {
		return maxCerencia;
	}
	public void setMaxCerencia(float maxCerencia) {
		this.maxCerencia = maxCerencia;
	}
	public float getMaxHr() {
		return maxHr;
	}
	public void setMaxHr(float maxHr) {
		this.maxHr = maxHr;
	}
}
