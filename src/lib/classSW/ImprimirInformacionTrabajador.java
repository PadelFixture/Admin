package lib.classSW;

public class ImprimirInformacionTrabajador {
	
	private String listaTrabajador;
	private String fechaContratacion;
	private String fechaPactoHorasExtra;
	private String idContrato;
	private String fechaTerminoDocumento;
	
	public String getListaTrabajador() {
		return listaTrabajador;
	}
	public void setListaTrabajador(String listaTrabajador) {
		this.listaTrabajador = listaTrabajador;
	}
	public String getFechaContratacion() {
		return fechaContratacion;
	}
	public void setFechaContratacion(String fechaContratacion) {
		this.fechaContratacion = fechaContratacion;
	}
	public String getFechaPactoHorasExtra() {
		return fechaPactoHorasExtra;
	}
	public void setFechaPactoHorasExtra(String fechaPactoHorasExtra) {
		this.fechaPactoHorasExtra = fechaPactoHorasExtra;
	}
	public String getIdContrato() {
		return idContrato;
	}
	public void setIdContrato(String idContrato) {
		this.idContrato = idContrato;
	}
	public String getFechaTerminoDocumento() {
		return fechaTerminoDocumento;
	}
	public void setFechaTerminoDocumento(String fechaTerminoDocumento) {
		this.fechaTerminoDocumento = fechaTerminoDocumento;
	}

	
	
}
