package lib.classSA;

public class estimacion_semana {
	private int codigo;
	private int codigo_anual;
	private String semana;
	private int valor;
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public int getCodigo_anual() {
		return codigo_anual;
	}
	public void setCodigo_anual(int codigo_anual) {
		this.codigo_anual = codigo_anual;
	}
	public String getSemana() {
		return semana;
	}
	public void setSemana(String semana) {
		this.semana = semana;
	}
	public int getValor() {
		return valor;
	}
	public void setValor(int valor) {
		this.valor = valor;
	}
	
	
}
