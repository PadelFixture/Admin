package lib.classSA;

public class TEMPORADA {

	public int codigo;
	public String descripcion;
	public int ano_inicio;
	public int ano_termino;
	public String fecha_inicio;
	public String fecha_termino;
	
	
	
	public int getCodigo() {
		return codigo;
	}
	public int getAno_inicio() {
		return ano_inicio;
	}
	public void setAno_inicio(int ano_inicio) {
		this.ano_inicio = ano_inicio;
	}
	public int getAno_termino() {
		return ano_termino;
	}
	public void setAno_termino(int ano_termino) {
		this.ano_termino = ano_termino;
	}
	public String getFecha_inicio() {
		return fecha_inicio;
	}
	public void setFecha_inicio(String fecha_inicio) {
		this.fecha_inicio = fecha_inicio;
	}
	public String getFecha_termino() {
		return fecha_termino;
	}
	public void setFecha_termino(String fecha_termino) {
		this.fecha_termino = fecha_termino;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
		
}
