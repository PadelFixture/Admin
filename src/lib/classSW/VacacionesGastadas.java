package lib.classSW;

public class VacacionesGastadas {

	private String idVacacionesGastadas;
	private String fechaInicio;
	private String fechaTermino;
	private String estado;
	private String idVacaciones;
	private String sociedad;
	private String codTrabajador;
	private String idContrato;
	
	public String getIdVacacionesGastadas() {
		return idVacacionesGastadas;
	}
	public void setIdVacacionesGastadas(String idVacacionesGastadas) {
		this.idVacacionesGastadas = idVacacionesGastadas;
	}
	public String getFechaInicio() {
		return fechaInicio;
	}
	public void setFechaInicio(String fechaInicio) {
		this.fechaInicio = fechaInicio;
	}
	public String getFechaTermino() {
		return fechaTermino;
	}
	public void setFechaTermino(String fechaTermino) {
		this.fechaTermino = fechaTermino;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getIdVacaciones() {
		return idVacaciones;
	}
	public void setIdVacaciones(String idVacaciones) {
		this.idVacaciones = idVacaciones;
	}
	public String getSociedad() {
		return sociedad;
	}
	public void setSociedad(String sociedad) {
		this.sociedad = sociedad;
	}
	public String getCodTrabajador() {
		return codTrabajador;
	}
	public void setCodTrabajador(String codTrabajador) {
		this.codTrabajador = codTrabajador;
	}
	public String getIdContrato() {
		return idContrato;
	}
	public void setIdContrato(String idContrato) {
		this.idContrato = idContrato;
	}
	
}
