package lib.classSW;

public class RutNoAutorizados {
	
	private int idRutNoAutorizados;
	private String rutNoAutorizados;
	private String autorizados;
	private String usuario;
	private String codigoGenerado;
	
	public int getIdRutNoAutorizados() {
		return idRutNoAutorizados;
	}
	public void setIdRutNoAutorizados(int idRutNoAutorizados) {
		this.idRutNoAutorizados = idRutNoAutorizados;
	}
	public String getRutNoAutorizados() {
		return rutNoAutorizados;
	}
	public void setRutNoAutorizados(String rutNoAutorizados) {
		this.rutNoAutorizados = rutNoAutorizados;
	}
	public String getAutorizados() {
		return autorizados;
	}
	public void setAutorizados(String autorizados) {
		this.autorizados = autorizados;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getCodigoGenerado() {
		return codigoGenerado;
	}
	public void setCodigoGenerado(String codigoGenerado) {
		this.codigoGenerado = codigoGenerado;
	}
	 
	 


}
