package lib.classSW;

public class TurnosEmpresa {
	
	int id;
	String empresa;
	String huerto;
	String zona;
	String nombre;
	String nombre_empresa;
	String nombre_huerto;
	
	
	
	public String getNombre_huerto() {
		return nombre_huerto;
	}
	public void setNombre_huerto(String nombre_huerto) {
		this.nombre_huerto = nombre_huerto;
	}
	public String getNombre_empresa() {
		return nombre_empresa;
	}
	public void setNombre_empresa(String nombre_empresa) {
		this.nombre_empresa = nombre_empresa;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getEmpresa() {
		return empresa;
	}
	public void setEmpresa(String empresa) {
		this.empresa = empresa;
	}
	public String getHuerto() {
		return huerto;
	}
	public void setHuerto(String huerto) {
		this.huerto = huerto;
	}
	public String getZona() {
		return zona;
	}
	public void setZona(String zona) {
		this.zona = zona;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	

}
