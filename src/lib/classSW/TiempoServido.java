package lib.classSW;

public class TiempoServido {
	public String empresa;
	public int periodo;
	public int sueldo_base;
	public int gratificacion;
	public int total_imponible;
	public Double dias;
	public String getEmpresa() {
		return empresa;
	}
	public void setEmpresa(String empresa) {
		this.empresa = empresa;
	}
	public int getPeriodo() {
		return periodo;
	}
	public void setPeriodo(int periodo) {
		this.periodo = periodo;
	}
	public int getSueldo_base() {
		return sueldo_base;
	}
	public void setSueldo_base(int sueldo_base) {
		this.sueldo_base = sueldo_base;
	}
	public int getGratificacion() {
		return gratificacion;
	}
	public void setGratificacion(int gratificacion) {
		this.gratificacion = gratificacion;
	}
	public int getTotal_imponible() {
		return total_imponible;
	}
	public void setTotal_imponible(int total_imponible) {
		this.total_imponible = total_imponible;
	}
	public Double getDias() {
		return dias;
	}
	public void setDias(Double dias) {
		this.dias = dias;
	}
	
	

}
