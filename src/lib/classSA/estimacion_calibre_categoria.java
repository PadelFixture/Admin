package lib.classSA;

public class estimacion_calibre_categoria {
	private int codigo;
	private int codigo_anual;
	private String categoria;
	private String calibre;
	private int anual;
	private int semana;
	private int editado;
	private int valor;
	private int usuario;
	private String fecha;
	
	
	
	
	public int getSemana() {
		return semana;
	}
	public void setSemana(int semana) {
		this.semana = semana;
	}
	public int getAnual() {
		return anual;
	}
	public void setAnual(int anual) {
		this.anual = anual;
	}
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public int getCodigo_anual() {
		return codigo_anual;
	}
	public void setCodigo_anual(int codigo_anual) {
		this.codigo_anual = codigo_anual;
	}
	public String getCategoria() {
		return categoria;
	}
	public void setCategoria(String categoria) {
		this.categoria = categoria;
	}
	public String getCalibre() {
		return calibre;
	}
	public void setCalibre(String calibre) {
		this.calibre = calibre;
	}
	
	
	public int getEditado() {
		return editado;
	}
	public void setEditado(int editado) {
		this.editado = editado;
	}
	public int getValor() {
		return valor;
	}
	public void setValor(int valor) {
		this.valor = valor;
	}
	public int getUsuario() {
		return usuario;
	}
	public void setUsuario(int usuario) {
		this.usuario = usuario;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	
	
}
