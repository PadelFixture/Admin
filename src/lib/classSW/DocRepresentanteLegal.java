package lib.classSW;

public class DocRepresentanteLegal {

	private String ciudadEmpresa;
	private String direccionEmpresa;
	private String representanteEmpresa;
	private String representanteTelefono;
	private String representanteRut;
	private String comunaEmpresa;
	private String representanteNombre;
	
	public String getCiudadEmpresa() {
		return ciudadEmpresa;
	}
	public void setCiudadEmpresa(String ciudadEmpresa) {
		this.ciudadEmpresa = ciudadEmpresa;
	}
	public String getDireccionEmpresa() {
		return direccionEmpresa;
	}
	public void setDireccionEmpresa(String direccionEmpresa) {
		this.direccionEmpresa = direccionEmpresa;
	}
	public String getRepresentanteEmpresa() {
		return representanteEmpresa;
	}
	public void setRepresentanteEmpresa(String representanteEmpresa) {
		this.representanteEmpresa = representanteEmpresa;
	}
	public String getRepresentanteTelefono() {
		return representanteTelefono;
	}
	public void setRepresentanteTelefono(String representanteTelefono) {
		this.representanteTelefono = representanteTelefono;
	}
	public String getRepresentanteRut() {
		return representanteRut;
	}
	public void setRepresentanteRut(String representanteRut) {
		this.representanteRut = representanteRut;
	}
	public String getComunaEmpresa() {
		return comunaEmpresa;
	}
	public void setComunaEmpresa(String comunaEmpresa) {
		this.comunaEmpresa = comunaEmpresa;
	}
	public String getRepresentanteNombre() {
		return representanteNombre;
	}
	public void setRepresentanteNombre(String representanteNombre) {
		this.representanteNombre = representanteNombre;
	}
	
	
	
	
}
