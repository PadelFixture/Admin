package lib.classSA;

public class EstimacionIngreso {
	private int codigo;
	private int codigo_anual;
	private int exportacion;
	private int merma;
	private int comercial;
	
	
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public int getCodigo_anual() {
		return codigo_anual;
	}
	public void setCodigo_anual(int codigo_anual) {
		this.codigo_anual = codigo_anual;
	}
	public int getExportacion() {
		return exportacion;
	}
	public void setExportacion(int exportacion) {
		this.exportacion = exportacion;
	}
	public int getMerma() {
		return merma;
	}
	public void setMerma(int merma) {
		this.merma = merma;
	}
	public int getComercial() {
		return comercial;
	}
	public void setComercial(int comercial) {
		this.comercial = comercial;
	}
	
	
	
}
