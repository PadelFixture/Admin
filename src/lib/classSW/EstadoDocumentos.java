package lib.classSW;

public class EstadoDocumentos {

	private String idEstadoContrato;
	private String codTrabajador;
	private String idContrato;
	private String periodo;
	private String documento;
	private String impreso;
	private String entregado;
	private String firmado;
	
	
	public String getIdEstadoContrato() {
		return idEstadoContrato;
	}
	public void setIdEstadoContrato(String idEstadoContrato) {
		this.idEstadoContrato = idEstadoContrato;
	}
	public String getCodTrabajador() {
		return codTrabajador;
	}
	public void setCodTrabajador(String codTrabajador) {
		this.codTrabajador = codTrabajador;
	}
	public String getIdContrato() {
		return idContrato;
	}
	public void setIdContrato(String idContrato) {
		this.idContrato = idContrato;
	}
	public String getPeriodo() {
		return periodo;
	}
	public void setPeriodo(String periodo) {
		this.periodo = periodo;
	}
	public String getDocumento() {
		return documento;
	}
	public void setDocumento(String documento) {
		this.documento = documento;
	}
	public String getImpreso() {
		return impreso;
	}
	public void setImpreso(String impreso) {
		this.impreso = impreso;
	}
	public String getEntregado() {
		return entregado;
	}
	public void setEntregado(String entregado) {
		this.entregado = entregado;
	}
	public String getFirmado() {
		return firmado;
	}
	public void setFirmado(String firmado) {
		this.firmado = firmado;
	}
	
	
	
}
