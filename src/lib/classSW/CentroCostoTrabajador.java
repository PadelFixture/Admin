package lib.classSW;

public class CentroCostoTrabajador {

	private int idCentroCosto;
	private int codTrabajador;
	private String sociedad;
	private String idHuerto;
	private String idCECO;
	private String ordenCO;
	private String cuenta;
	private double pesos;
	private double porcentaje;
	
	public int getIdCentroCosto() {
		return idCentroCosto;
	}
	public void setIdCentroCosto(int idCentroCosto) {
		this.idCentroCosto = idCentroCosto;
	}
	public int getCodTrabajador() {
		return codTrabajador;
	}
	public void setCodTrabajador(int codTrabajador) {
		this.codTrabajador = codTrabajador;
	}
	public String getSociedad() {
		return sociedad;
	}
	public void setSociedad(String sociedad) {
		this.sociedad = sociedad;
	}
	public String getIdHuerto() {
		return idHuerto;
	}
	public void setIdHuerto(String idHuerto) {
		this.idHuerto = idHuerto;
	}
	public String getIdCECO() {
		return idCECO;
	}
	public void setIdCECO(String idCECO) {
		this.idCECO = idCECO;
	}
	public String getOrdenCO() {
		return ordenCO;
	}
	public void setOrdenCO(String ordenCO) {
		this.ordenCO = ordenCO;
	}
	public String getCuenta() {
		return cuenta;
	}
	public void setCuenta(String cuenta) {
		this.cuenta = cuenta;
	}
	public double getPesos() {
		return pesos;
	}
	public void setPesos(double pesos) {
		this.pesos = pesos;
	}
	public double getPorcentaje() {
		return porcentaje;
	}
	public void setPorcentaje(double porcentaje) {
		this.porcentaje = porcentaje;
	}
		
}
